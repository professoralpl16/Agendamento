public void preencheTabela()
    {
        C_Combustivel cc = new C_Combustivel();
        tabela = cc.dadosTabela(txtPesquisa.getText());
        
        
        String titulo[] = {"Código","Tipo"};
        //tabela = ce.consultaDados();
        
        
        
        CarregaTabelas ct = new CarregaTabelas();
        DefaultTableModel model = ct.PreencheTabela(titulo, tabela);
              
        total = 0;
        tabelaCombustivel.setModel(model);
        try {
            tabela.beforeFirst();
            
            while(tabela.next())
            {
                total++;
            }
            
            tabela.first();
        } catch (SQLException ex) {
            Logger.getLogger(Combustivel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


