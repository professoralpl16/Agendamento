    private void bntEscluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEscluirActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Gostaria de alterar esse Documento?");
        if(confirm == 0){
        
        M_Combustivel mc = new M_Combustivel();
        mc.setId_combustivel(id_Combustivel);
        
        
        C_Combustivel cc = new C_Combustivel();
        cc.excluiSql((Object)mc);
        
        preencheTabela();
        tabelaCombustivel.setRowSelectionInterval(linha, 0);
        }
    }//GEN-LAST:event_bntEscluirActionPerformed

