        M_Paciente mp = new M_Paciente();
        
        radioSexo();
        radioCivil();
        
        
        
        
        
       
        if("".equals(txtNome.getText()))
            {JOptionPane.showMessageDialog(null, "Campo Nome é Obrigatorios!!!");}
