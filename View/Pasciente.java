    private void imagemFotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imagemFotoMouseClicked
        // TODO add your handling code here:
        JFileChooser arquivo = new JFileChooser();
        int janela = arquivo.showOpenDialog(null);
        if(janela == JFileChooser.APPROVE_OPTION){
            File file = arquivo.getSelectedFile();
            txtnomeimagem.setText(String.valueOf(file));          
            Image foto = getToolkit().getImage(txtnomeimagem.getText());
            foto = foto.getScaledInstance(150, 150, Image.SCALE_DEFAULT);
            imagemFoto.setIcon(new ImageIcon(foto));
        }
        
        
    }//GEN-LAST:event_imagemFotoMouseClicked

