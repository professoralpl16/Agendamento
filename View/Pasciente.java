    public void CarregaComboEstado()
    {
          C_Estado cest = new C_Estado();
        
        dadosEstado = cest.retornaCombo();
        
        int i = 0;
        
        cmbEstado.removeAllItems();
        while(i<dadosEstado.size())
        {
            cmbEstado.addItem(dadosEstado.get(i).getNomeestado());
            i++;
        }
    }
