    public void CarregaComboMarcaVeiculo()
    {
          C_MarcaVeiculo ce = new C_MarcaVeiculo();
        
        dadosMarcaVeiculo = ce.retornaCombo();
        
        int i = 0;
        
        cmbMarca.removeAllItems();
        while(i<dadosMarcaVeiculo.size())
        {
            cmbMarca.addItem(dadosMarcaVeiculo.get(i).getMarca());
            i++;
        }
    }
    public void CarregaComboCombustivel()
    {
          C_Combustivel ce = new C_Combustivel();
        
        dadosCombustivel = ce.retornaCombo();
        
        int i = 0;
        
        cmbCombustivel.removeAllItems();
        while(i<dadosCombustivel.size())
        {
            cmbCombustivel.addItem(dadosCombustivel.get(i).getTipo());
            i++;
        }
    }

