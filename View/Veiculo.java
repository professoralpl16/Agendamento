    private void tabelaVeiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaVeiculoMouseClicked
        int linha = tabelaVeiculo.getSelectedRow();
        //v.id_veiculo, v.placa, v.anofabricacao, v.anomodelo, v.chassi, 
        //v.renavam, v.qtdpassageiros,v.patrimonio, m.marca, v.modelo, v.cor, c.tipo
        id_veiculo = Integer.parseInt(tabelaVeiculo.getValueAt(linha, 0).toString());
        placa = tabelaVeiculo.getValueAt(linha, 1).toString();
        anof = tabelaVeiculo.getValueAt(linha, 2).toString();
        anom = tabelaVeiculo.getValueAt(linha, 3).toString();
        chassi = tabelaVeiculo.getValueAt(linha, 4).toString();
        renavam = tabelaVeiculo.getValueAt(linha, 5).toString();
        passag = tabelaVeiculo.getValueAt(linha, 6).toString();
        patrimonio = tabelaVeiculo.getValueAt(linha, 7).toString();
        marca = tabelaVeiculo.getValueAt(linha, 8).toString();
        modelo = tabelaVeiculo.getValueAt(linha, 9).toString();
        cor = tabelaVeiculo.getValueAt(linha, 10).toString();
        combustivel = tabelaVeiculo.getValueAt(linha, 11).toString();
        
        txtVeiculo.setText(modelo);
        cmbMarca.setSelectedItem(marca);
        cmbCombustivel.setSelectedItem(combustivel);
        txtPassageiro.setText(passag);
        txtCor.setText(cor);
        txtRenavam.setText(renavam);
        txtChassi.setText(chassi);
        txtAno.setText(anof);
        txtAnoModelo.setText(anom);
        txtPlaca.setText(placa);
        txtPatrimonio.setText(patrimonio);
    }//GEN-LAST:event_tabelaVeiculoMouseClicked

